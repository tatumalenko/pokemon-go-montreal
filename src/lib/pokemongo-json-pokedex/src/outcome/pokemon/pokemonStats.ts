/**
 * Data about the statistics of the Pokemon
 */
export interface PokemonStats {
  /**
   * The base stamina of the Pokemon
   * @example
   * 80
   */
  baseStamina: number;
  /**
   * The base attack of the Pokemon
   * @example
   * 63
   */
  baseAttack: number;
  /**
   * The base defense of the Pokemon
   * @example
   * 55
   */
  baseDefense: number;
}